// constructor function: should create a new scooter with three properties
function  Scooter (year, color, model) {
  this.year = year;
  this.color = color;
  this.model  = model;
}


// constructor function: should create a new driver with three properties
function Driver (name, age, experience) {
  this.age = age;
  this.name = name;
  this.experience = experience;
}


// constructor function: should create a new PickupLocation with properties
function PickupLocation (address, city) {
  this.address = address;
  this.city = city;
}
